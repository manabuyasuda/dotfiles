#!/usr/bin/env bash
# statusline.sh の年収換算ロジックの回帰テスト
#
# 守りたい不変条件:
#   「同じ作業（同じ累計コスト）なら、昼休憩などの中断を挟んでも年収換算がほぼ一定」
#
# 中断ギャップは total_duration_ms（壁時計）の前回値との差分で検出するため、実時計
# （date）に依存せず、同じ入力列なら出力が決定的になる。よって CI で再現できる。
# statusline.sh はイベント駆動で完全アイドル時には呼ばれない前提に基づく
# （前回実行から今回までのギャップ＝離席時間とみなせる）。
#
# 使い方: bash claude/tests/statusline.test.sh
# 終了コード 0=全テスト成功 / 1=失敗あり

set -u

SCRIPT_DIR=$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)
STATUSLINE="$SCRIPT_DIR/../statusline.sh"

# テスト専用の隔離キャッシュ置き場（本物の TMPDIR を汚さない）。
# statusline.sh は ${TMPDIR}/statusline-prev-<session_id> に状態を持ち越すため、
# TMPDIR を差し替えるとセッションごとに独立したキャッシュになる。
TEST_TMP=$(mktemp -d)
trap 'rm -rf "$TEST_TMP"' EXIT
export TMPDIR="$TEST_TMP"

PASS=0
FAIL=0

# cost 行には ANSI カラーコードが付くので、文字列照合の前に除去する
_strip_ansi() { sed 's/\x1b\[[0-9;]*m//g'; }

# statusline.sh を1回実行する。JSON を stdin に渡し、色を除いた出力を返す。
# session_id を固定して連続実行すると、同じキャッシュを使い回して状態が持ち越される
# （＝Claude Code が同一セッション中に繰り返し statusline を呼ぶ状況の再現）。
_run() {
  printf '%s' "$1" | bash "$STATUSLINE" | _strip_ansi
}

# session_id・累計コスト(USD)・累計経過時間(ミリ秒) から最小限の入力 JSON を作る
_json() {
  printf '{"session_id":"%s","cost":{"total_cost_usd":%s,"total_duration_ms":%s}}' "$1" "$2" "$3"
}

_assert_contains() {
  local desc="$1" haystack="$2" needle="$3"
  if printf '%s' "$haystack" | grep -qF "$needle"; then
    PASS=$((PASS + 1)); printf 'ok   - %s\n' "$desc"
  else
    FAIL=$((FAIL + 1)); printf 'FAIL - %s\n       期待: 「%s」を含む\n       実際: %s\n' "$desc" "$needle" "$haystack"
  fi
}

_assert_not_contains() {
  local desc="$1" haystack="$2" needle="$3"
  if printf '%s' "$haystack" | grep -qF "$needle"; then
    FAIL=$((FAIL + 1)); printf 'FAIL - %s\n       期待: 「%s」を含まない\n       実際: %s\n' "$desc" "$needle" "$haystack"
  else
    PASS=$((PASS + 1)); printf 'ok   - %s\n' "$desc"
  fi
}

# ---------------------------------------------------------------------------
# T1: 昼休憩の有無で年収換算が変わらない（このリポジトリの中心的な不変条件）
#
# 連続作業60分・累計$2.00 と、同じ作業に60分の昼休憩を挟んだ場合（累計$2.00）で、
# どちらも有効作業時間=60分 → 年収換算が同じ「約60万円」になること。
#   annual = 2.00 / (60/60)h × 2000 × 160 = 640,000 → 10万円丸めで 60万円
# 修正前は休憩ありが分母120分になり「約32万円」へ下振れしていた。
# 10分刻み（10分 < 15分閾値）の通常ギャップではアイドルを差し引かない点も同時に担保する。
# ---------------------------------------------------------------------------
# セッション A: 中断なし。10分刻みで60分まで
_run "$(_json A 0    0)"       >/dev/null
_run "$(_json A 0.33 600000)"  >/dev/null
_run "$(_json A 0.67 1200000)" >/dev/null
_run "$(_json A 1.00 1800000)" >/dev/null
_run "$(_json A 1.33 2400000)" >/dev/null
_run "$(_json A 1.67 3000000)" >/dev/null
out_a=$(_run "$(_json A 2.00 3600000)")
_assert_contains "T1-a 連続60分は約60万円" "$out_a" "年収換算 約60万円"

# セッション B: 30分作業 → 60分休憩（コスト据え置き）→ 30分作業
_run "$(_json B 0    0)"       >/dev/null
_run "$(_json B 0.33 600000)"  >/dev/null
_run "$(_json B 0.67 1200000)" >/dev/null
_run "$(_json B 1.00 1800000)" >/dev/null   # 30分時点 $1.00
_run "$(_json B 1.00 5400000)" >/dev/null   # +60分休憩でコスト据え置き（90分時点）→ 60分を除外
_run "$(_json B 1.33 6000000)" >/dev/null   # 100分
_run "$(_json B 1.67 6600000)" >/dev/null   # 110分
out_b=$(_run "$(_json B 2.00 7200000)")      # 120分・$2.00、有効作業=60分
_assert_contains "T1-b 昼休憩を挟んでも約60万円" "$out_b" "年収換算 約60万円"

# ---------------------------------------------------------------------------
# T2: 有効作業時間が10分ゲート未満なら年収換算を出さない（費用行は出す）
# ---------------------------------------------------------------------------
_run "$(_json C 0 0)" >/dev/null
out_c=$(_run "$(_json C 0.5 300000)")   # 5分（< 10分ゲート）
_assert_not_contains "T2-a ゲート未満は年収換算を出さない" "$out_c" "年収換算"
_assert_contains    "T2-b 費用行（¥）は出る"             "$out_c" "¥"

# ---------------------------------------------------------------------------
# T3: 閾値以下の小ギャップは除外しない（誤差レベルの離席は作業時間に残す）
#
# 10分刻み（< 15分閾値）だけで進めると、アイドルは差し引かれず有効作業=20分。
#   annual = 2.0 / (20/60)h × 2000 × 160 = 1,920,000 → 10万円丸めで 190万円
# もし10分ギャップを誤って中断除外すると有効作業=10分 → 約380万円になるため区別できる。
# ---------------------------------------------------------------------------
_run "$(_json D 0 0)" >/dev/null
_run "$(_json D 1.0 600000)"  >/dev/null  # 10分（gap 10分 < 15分 → 除外しない）
out_d=$(_run "$(_json D 2.0 1200000)")     # 20分（gap 10分 < 15分 → 除外しない）
_assert_contains "T3 小ギャップは除外せず約190万円" "$out_d" "年収換算 約190万円"

# ---------------------------------------------------------------------------
# T4: 中断除外後も有効作業時間が負にならず、年収換算が破綻しない
#
# 10分作業 → 120分休憩（コスト据え置き）→ 10分作業。累計経過=140分・休憩120分を除外し
# 有効作業=20分。annual は T3 と同じ計算で約190万円（ゲート通過・分母は正）。
# ---------------------------------------------------------------------------
_run "$(_json E 0 0)" >/dev/null
_run "$(_json E 1.0 600000)"  >/dev/null   # 10分・$1.0
_run "$(_json E 1.0 7800000)" >/dev/null   # +120分休憩（130分時点・コスト据え置き）→ 120分を除外
out_e=$(_run "$(_json E 2.0 8400000)")      # 140分・$2.0、有効作業=20分
_assert_contains "T4 長時間休憩後も分母が正で約190万円" "$out_e" "年収換算 約190万円"

# ---------------------------------------------------------------------------
echo "----"
printf '成功 %d / 失敗 %d\n' "$PASS" "$FAIL"
[ "$FAIL" -eq 0 ]
